// 02 알고리즘 설계가 더 중요하다. 알고리즘은 문제를 해결하는 절차와 방법으로,
//    설계가 잘못되면 코딩을 아무리 잘해도 올바른 결과를 얻을 수 없기 때문이다.
//    코딩은 설계된 알고리즘을 C++ 언어로 표현하는 수단에 불과하다.

// 03 클래스(Class)는 객체를 만드는 틀로, 데이터(멤버 변수)와 함수(멤버 함수)를
//    하나로 묶은 사용자 정의 자료형이다.
//    객체(Object)는 클래스라는 틀에서 생성된 실체(instance)이며 클래스형 변수이다.

// 04 절차지향 프로그래밍 언어는 실행할 절차대로 명령어를 나열하고 흐름도를 중심으로
//    설계하는 방식이며 C, Pascal 등이 있다.
//    객체지향 프로그래밍 언어는 객체를 정의하고 객체들의 상호작용으로 구현하는 방식이며
//    C++, Java 등이 있다.

// 05 라이브러리는 프로그램 개발 시 자주 사용되는 기능(printf, cin, cout 등)을
//    미리 작성해 놓은 코드의 집합이다. C 라이브러리, C++ 입출력 라이브러리,
//    C++ STL 라이브러리로 구성된다.

// 06 라이브러리는 이미 컴파일된 목적파일 형태로 존재하는데, 소스코드를 공개하지 않고
//    기능만 제공할 수 있으며, 매번 다시 컴파일할 필요 없이 링크 과정에서 바로
//    연결하여 사용할 수 있기 때문이다.

// 07 컴파일 단계에서 생성된 목적파일(.obj)은 바로 실행할 수 없다.
//    링크 과정은 내가 작성한 목적파일과 C++ 표준 라이브러리 목적파일을
//    링커(Linker)가 하나로 연결하여 최종 실행파일(.exe)을 만드는 과정이기 때문이다.
